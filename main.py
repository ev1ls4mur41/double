def duplicate_max_digit(n):
    # Преобразование числа в список цифр
    digits = [int(d) for d in str(n)]
    # Нахождение максимальной цифры в числе
    max_digit = max(digits)
    # Инициализация результирующей строки
    result = ""
    # Обход каждой цифры в числе
    for digit in digits:
        # Если цифра равна максимальной цифре в числе,
        # то добавляем ее дважды в результирующую строку
        if digit == max_digit:
            result += str(digit) * 2
        # Иначе добавляем цифру в результирующую строку
        else:
            result += str(digit)
    # Преобразование результирующей строки в число и возврат его
    return int(result)

# Запрос у пользователя натурального числа
n = int(input("Введите натуральное число: "))
# Вызов функции и вывод результата
result = duplicate_max_digit(n)
print(f"Результат: {result}")